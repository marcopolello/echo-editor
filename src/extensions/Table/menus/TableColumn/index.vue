<script setup lang="ts">
import { computed } from 'vue'
import type { Editor } from '@tiptap/vue-3'
import { BubbleMenu } from '@tiptap/vue-3'
import { Editor as CoreEditor } from '@tiptap/core'
import { EditorState } from '@tiptap/pm/state'
import { EditorView } from '@tiptap/pm/view'
import type { NodeSelection } from '@tiptap/pm/state'
import { CellSelection } from '@tiptap/pm/tables'
import { analyzeCellSelection, isColumnGripSelected } from '../../utils'
import ActionButton from '@/components/ActionButton.vue'
import HighlightActionButton from '@/extensions/Highlight/components/HighlightActionButton.vue'

interface Props {
  editor: Editor
}
export interface ShouldShowProps {
  editor?: CoreEditor
  view: EditorView
  state?: EditorState
  oldState?: EditorState
  from?: number
  to?: number
}

const props = withDefaults(defineProps<Props>(), {})

export interface Emits {
  (event: 'onMergeCell'): void
  (event: 'onSplitCell'): void
  (event: 'setCellBackground', value: string): void
}
const emits = defineEmits<Emits>()

const shouldShow = ({ view, state, from }: ShouldShowProps) => {
  if (!state) {
    return false
  }
  return isColumnGripSelected({
    editor: props.editor,
    view,
    state,
    from: from || 0,
  })
}

const Selection = computed(() => {
  const NodeSelection = props.editor.state.selection as NodeSelection
  const isCell = NodeSelection instanceof CellSelection
  if (isCell) {
    return analyzeCellSelection(props.editor)
  }
  {
    return null
  }
})

function onAddColumnBefore() {
  props.editor.chain().focus().addColumnBefore().run()
}

function onAddColumnAfter() {
  props.editor.chain().focus().addColumnAfter().run()
}

function onDeleteColumn() {
  props.editor.chain().focus().deleteColumn().run()
}
</script>

<template>
  <BubbleMenu
    :editor="editor"
    pluginKey="tableColumnMenu"
    :updateDelay="0"
    :should-show="shouldShow"
    :tippy-options="{
      appendTo: 'parent',
      offset: [0, 15],
      popperOptions: {
        modifiers: [{ name: 'flip', enabled: false }],
      },
    }"
  >
    <div
      class="min-w-32 flex flex-row h-full leading-none gap-0.5 p-2 bg-background rounded-lg shadow-sm border border-border"
    >
      <ActionButton
        icon="BetweenHorizonalEnd"
        :action="onAddColumnBefore"
        tooltip="inoltrareinserireelenco"
        :tooltip-options="{
          sideOffset: 15,
        }"
      />
      <ActionButton
        icon="BetweenHorizonalStart"
        :action="onAddColumnAfter"
        tooltip="indietroinserireelenco"
        :tooltip-options="{
          sideOffset: 15,
        }"
      />

      <ActionButton
        icon="Trash2"
        :action="onDeleteColumn"
        tooltip="eliminareelenco"
        :tooltip-options="{
          sideOffset: 15,
        }"
      />
      <ActionButton
        v-if="Selection?.cellCount! > 1"
        icon="TableCellsMerge"
        :action="() => emits('onMergeCell')"
        tooltip="unisci celle"
        :tooltip-options="{
          sideOffset: 15,
        }"
      />
      <ActionButton
        v-if="Selection?.mergedCellCount! > 0"
        icon="TableCellsSplit"
        :action="() => emits('onSplitCell')"
        tooltip="Cella divisa"
        :tooltip-options="{
          sideOffset: 15,
        }"
      />
      <HighlightActionButton
        :editor="editor"
        tooltip="colore background"
        :action="color => emits('setCellBackground', color as string)"
        :tooltip-options="{
          sideOffset: 15,
        }"
      />
    </div>
  </BubbleMenu>
</template>
